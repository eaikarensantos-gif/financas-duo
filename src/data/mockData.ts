// ─── Categorization rules engine ─────────────────────────────────────────────
// Maps keyword patterns in bank description → { category, subcategory }
export const categorizationRules: {
  pattern: RegExp
  category: string
  subcategory: string
  profile: 'PF' | 'PJ' | 'both'
}[] = [
  // PF
  { pattern: /mercado|supermercado|ifood|rappi|delivery|restaurante/i,   category: 'Alimentação',  subcategory: 'Mercado/Delivery', profile: 'PF' },
  { pattern: /aluguel|condomínio|condominio/i,                            category: 'Moradia',      subcategory: 'Aluguel/Condomínio', profile: 'PF' },
  { pattern: /plano de saúde|farmácia|farmacia|drogaria|hospital/i,       category: 'Saúde',        subcategory: 'Plano/Farmácia', profile: 'PF' },
  { pattern: /netflix|spotify|steam|cinema|teatro|lazer/i,               category: 'Lazer',        subcategory: 'Entretenimento', profile: 'PF' },
  { pattern: /uber|99|táxi|taxi|metrô|metro|ônibus|onibus|combustível/i,  category: 'Transporte',   subcategory: 'Mobilidade', profile: 'PF' },
  { pattern: /curso|faculdade|escola|livro|educação|educacao/i,           category: 'Educação',     subcategory: 'Cursos/Livros', profile: 'PF' },
  // PJ
  { pattern: /das|darf|simples nacional|imposto/i,                        category: 'Impostos',     subcategory: 'DAS/DARF', profile: 'PJ' },
  { pattern: /software|saas|github|aws|google cloud|vercel|notion/i,      category: 'Software/SaaS', subcategory: 'Assinaturas', profile: 'PJ' },
  { pattern: /marketing|anúncio|anuncio|meta ads|google ads|trafego/i,    category: 'Marketing',    subcategory: 'Anúncios', profile: 'PJ' },
  { pattern: /contabilidade|contador|contabil/i,                          category: 'Contabilidade', subcategory: 'Serviços', profile: 'PJ' },
  { pattern: /equipamento|notebook|computador|periférico/i,               category: 'Equipamentos', subcategory: 'Hardware', profile: 'PJ' },
  // Shared
  { pattern: /rendimento|dividendo|juros/i,                               category: 'Rendimento',   subcategory: 'Investimentos', profile: 'both' },
  { pattern: /transferência|transferencia|pix/i,                          category: 'Transferência', subcategory: 'Outros', profile: 'both' },
]

export function autoCategory(title: string, profile: 'PF' | 'PJ') {
  for (const rule of categorizationRules) {
    if (rule.pattern.test(title) && (rule.profile === profile || rule.profile === 'both')) {
      return { category: rule.category, subcategory: rule.subcategory }
    }
  }
  return { category: 'Outros', subcategory: undefined }
}

